#include<bits/stdc++.h>
#define ll long long
using namespace std;
 
int main(){
    ll w,k,n;
    cin >> k >> n >> w;
    ll sum = w*(w+1)/2;
    ll product = k*sum;
    if(n>product){
        cout << 0;
    }
    else{
        cout << product-n;
    }
    return 0;
}
